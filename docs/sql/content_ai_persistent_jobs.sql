  INDEX idx_content_ai_model_runs_status (status),
  INDEX idx_content_ai_model_runs_created_at (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
